 * @method static \Illuminate\Database\Eloquent\Builder|Tag whereUpdatedAt($value)
 * @mixin \Eloquent
 */
class Tag extends Model
{
    use HasFactory;

    public function descriptions()
    {
        return $this->belongsToMany(Description::class)->as('descriptions');
    }
}
